"""SafeMem 回归测试模块。"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from safemem.agents.baselines import (
    AllPolicyCleanAgent,
    AllPolicyNoisyAgent,
    CarriedPolicyAgent,
    NoPolicyAgent,
    OracleMinimalAgent,
    SummaryPolicyAgent,
)
from safemem.agents.msr_agent import MsrAgent, MsrNoisyAgent
from safemem.data import load_episodes, write_csv, write_json
from safemem.eval.judge import judge_result
from safemem.eval.metrics import summarize


def main() -> None:
    args = parse_args()
    agents = [
        NoPolicyAgent(),
        SummaryPolicyAgent(),
        CarriedPolicyAgent(),
        AllPolicyCleanAgent(),
        MsrAgent(),
        OracleMinimalAgent(),
        AllPolicyNoisyAgent(),
        MsrNoisyAgent(),
    ]

    summary_rows = []
    result_rows = []

    for language in args.languages:
        for irrelevant_count in args.irrelevant_counts:
            episode_path = (
                ROOT
                / args.episode_dir
                / f"mvp_plus_90_irrelevant_{irrelevant_count}_{language}.jsonl"
            )
            episodes = load_episodes(episode_path)
            results = []
            for episode in episodes:
                for agent in agents:
                    result = judge_result(episode, agent.decide(episode))
                    results.append(result)
                    result_rows.append(
                        {
                            "language": language,
                            "irrelevant_policies": irrelevant_count,
                            **result.to_dict(),
                        }
                    )

            for row in summarize(results):
                summary_rows.append(
                    {
                        "language": language,
                        "irrelevant_policies": irrelevant_count,
                        "episodes_path": str(episode_path.relative_to(ROOT)),
                        **row,
                    }
                )

    write_json(ROOT / "outputs" / "logs" / "policy_pressure_results.json", result_rows)
    write_csv(ROOT / "outputs" / "tables" / "policy_pressure_summary.csv", summary_rows)
    write_markdown_report(ROOT / "docs" / "policy_pressure_results.md", summary_rows)

    for row in summary_rows:
        print(row)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Run policy-pool pressure experiments.")
    parser.add_argument(
        "--episode-dir",
        default="data/episodes/pressure",
        help="Directory containing pressure JSONL files, relative to the repository root.",
    )
    parser.add_argument(
        "--languages",
        nargs="+",
        default=["en", "zh"],
        help="Episode languages to run.",
    )
    parser.add_argument(
        "--irrelevant-counts",
        nargs="+",
        type=int,
        default=[0, 10, 30, 50],
        help="Irrelevant policy counts to run.",
    )
    return parser.parse_args()


def write_markdown_report(path: Path, rows: list[dict[str, object]]) -> None:
    by_group: dict[tuple[str, int], dict[str, dict[str, object]]] = {}
    for row in rows:
        key = (str(row["language"]), int(row["irrelevant_policies"]))
        by_group.setdefault(key, {})[str(row["agent"])] = row

    lines = [
        "# Policy Pressure Results",
        "",
        "Generated by:",
        "",
        "```bash",
        "python experiments/run_policy_pressure.py",
        "```",
        "",
        "Each row summarizes 90 episodes. Safety metrics are emitted to `outputs/tables/policy_pressure_summary.csv`; this snapshot focuses on retrieval cost and retrieval quality.",
        "",
        "| Language | Irrelevant policies | all_policy_clean token cost | all_policy_clean coverage | all_policy_noisy token cost | all_policy_noisy coverage | carried_policy token cost | carried_policy coverage | oracle_minimal token cost | msr_clean token cost | msr_clean coverage | msr_noisy token cost | msr_noisy coverage |",
        "| --- | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: |",
    ]

    for language, irrelevant_count in sorted(by_group):
        group = by_group[(language, irrelevant_count)]
        all_clean = group["all_policy_clean"]
        all_noisy = group["all_policy_noisy"]
        carried = group["carried_policy"]
        oracle = group["oracle_minimal"]
        msr_clean = group["msr_clean"]
        msr_noisy = group["msr_noisy"]
        lines.append(
            "| "
            f"{language} | "
            f"{irrelevant_count} | "
            f"{_number(all_clean['avg_policy_token_cost'])} | "
            f"{_number(all_clean['avg_policy_coverage'])} | "
            f"{_number(all_noisy['avg_policy_token_cost'])} | "
            f"{_number(all_noisy['avg_policy_coverage'])} | "
            f"{_number(carried['avg_policy_token_cost'])} | "
            f"{_number(carried['avg_policy_coverage'])} | "
            f"{_number(oracle['avg_policy_token_cost'])} | "
            f"{_number(msr_clean['avg_policy_token_cost'])} | "
            f"{_number(msr_clean['avg_policy_coverage'])} | "
            f"{_number(msr_noisy['avg_policy_token_cost'])} | "
            f"{_number(msr_noisy['avg_policy_coverage'])} |"
        )

    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def _number(value: object) -> str:
    if isinstance(value, float):
        return f"{value:.2f}"
    return str(value)


if __name__ == "__main__":
    main()
